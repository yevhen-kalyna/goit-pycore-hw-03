"""Завдання 3: Нормалізація телефонних номерів для SMS-розсилки."""

import re


def normalize_phone(phone_number: str) -> str:
    """Нормалізує телефонний номер до стандартного формату.

    Видаляє всі зайві символи, залишаючи тільки цифри та символ '+' на початку.
    Якщо міжнародний код відсутній, автоматично додає код '+38' (Україна).

    Args:
        phone_number: Рядок з телефонним номером у довільному форматі.

    Returns:
        Нормалізований телефонний номер у вигляді рядка (наприклад, '+380501234567').
    """
    # Видаляємо всі символи, крім цифр та '+'
    cleaned = re.sub(r"[^\d+]", "", phone_number)

    # Додаємо міжнародний код, якщо він відсутній
    if cleaned.startswith("+"):
        pass  # код вже є
    elif cleaned.startswith("380"):
        cleaned = "+" + cleaned
    else:
        cleaned = "+38" + cleaned

    return cleaned


if __name__ == "__main__":
    raw_numbers = [
        "067\t123 4567",
        "(095) 234-5678\n",
        "+380 44 123 4567",
        "380501234567",
        "    +38(050)123-32-34",
        "     0503451234",
        "(050)8889900",
        "38050-111-22-22",
        "38050 111 22 11   ",
    ]

    sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
    print("Нормалізовані номери телефонів для SMS-розсилки:", sanitized_numbers)
